    private void showTableJadwal() throws SQLException {
        DefaultTableModel dtmJadwal = new DefaultTableModel(new String[]{
            "KODE JADWAL", "NAMA FILM", "TANGGAL TAYANG", "WAKTU TAYANG", "RUANG"
        }, 0);
        dtmJadwal.setRowCount(0);

        for (Jadwal jd : this.cinema.getDataJadwal()) {
            dtmJadwal.addRow(new String[]{
                jd.getKode_Jadwal().toString(),
                jd.getFilm().getNama_Film(),
                jd.getTgl_Tayang().toString(), 
                jd.getWktu_Tayang(),
                jd.getRuang()
            });
        }
        this.tabel2.setModel(dtmJadwal);
    }

    private void showComboBoxJadwal() throws SQLException {
        DefaultComboBoxModel dcbmJadwal = new DefaultComboBoxModel();

        for (Jadwal jd : this.cinema.getDataJadwal()) {
            dcbmJadwal.addElement(jd.getKode_Jadwal().toString() + ") " + jd.getFilm().getNama_Film());
        }
        this.item.setModel(dcbmJadwal);
        
    }

