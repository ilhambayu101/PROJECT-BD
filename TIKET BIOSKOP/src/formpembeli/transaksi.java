    private void printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printActionPerformed
        Transaksi trans = new Transaksi();
        try {
            datahave();
            trans.setreg(beli.getDataReg().get(Integer.parseInt(user1.getText())-1));
            trans.setKode_Tiket(Integer.parseInt(kodetiket.getText()));
            trans.setBanyak(Integer.parseInt(bnyk.getText()));
            trans.setarrHave(arrHV);
            
            this.beli.inserttrans(trans);
            new duduk(user.getText(),Integer.parseInt(kodetiket.getText())).setVisible(true);
            setVisible(false);
        } catch (SQLException err) {
            System.out.println(err);
        }
        
    }//GEN-LAST:event_printActionPerformed

    private void datahave()
    {
        try {
            
            Have hv = new Have();
            hv.setjdwl(this.cinema.getDataJadwal().get(item.getSelectedIndex()));
            this.hargatotal += (this.cinema.getDataJadwal().get(item.getSelectedIndex()).getFilm().getHarga() * Integer.parseInt(bnyk.getText()));
            hv.settotal(this.hargatotal);

            this.arrHV.add(hv);
        } catch (SQLException ex) {
            Logger.getLogger(transaksi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
        
